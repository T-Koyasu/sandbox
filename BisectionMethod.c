#include <stdio.h>
#include <math.h>
#define F(x)  exp(-0.5*x)-2*x*x			/*関数F（x）の定義*/
#define EPS 1.e-5						/*収束条件定義*/

int main(void)
{
	double a=0.,b=1.;
	double c,fa,fc;
	int i,n;
	n=log((b-a)/EPS)/log(2.)+0.5;
	fa=F(a);
	printf(" n		c		f(c)¥n");
	for(i=1;i<=n;i++){
		c=(a+b)/2;
		fc=F(c);
		printf("%2d%10.5f%10.5f¥n",i,c,fc);
		if(fa*fc<0.){
			b=c;
		}else{
			a=c;
		}
	}
	printf("¥n x=%7.5f¥n",c);
}
